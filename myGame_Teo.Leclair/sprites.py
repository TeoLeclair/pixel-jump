class Bomb(Sprite):
    def __init__(self, game, x, y, w, h, kind):
        Sprite.__init__(self)
        self.game = game
        self.image = pg.Surface((w, h))
        self.image = pg.image.load(os.path.join(img_folder, 'boom.png')).convert()
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.kind = kind
        self.pos = vec(WIDTH/2, HEIGHT/2)
        self.cd = Cooldown() #sets cooldwon for image timing
        self.tagged = False
    def seeking(self):
        if self.rect.x < self.game.player.rect.x:
            self.rect.x +=1
        if self.rect.x > self.game.player.rect.x:
            self.rect.x -=1
        if self.rect.y < self.game.player.rect.y:
            self.rect.y +=1
        if self.rect.y > self.game.player.rect.y:
            self.rect.y -=1
    def update(self):
        # print("updating")
        # self.seeking()
        self.cd.ticking()
        if self.cd.delta > .9 and self.tagged:
            # .9 is amount of time explosion stays on screen before ending
            print("time to die...")
            self.kill()
        
